package com.app.dto;

public class CommentCount {
	private Long CommentId;
	private int count;

	public Long getCommentId() {
		return CommentId;
	}
	public void setCommentId(Long CommentId) {
		this.CommentId = CommentId;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
}